<h3>How To Buy</h3>
<h3>Pembelian Online dari website</h3>
<p> - Pembelian online dapat langsung dilakukan dengan mengikuti prosedur yang tersedia di halaman transaksi kami</p>
<p> - Pilih produk-produk yang akan dibeli untuk dimasukkan ke dalam Cart (keranjang belanja) </p>
<p> - Lanjutkan proses pembelian dengan melakukan Checkout Now melalui halaman My Cart </p>
<p> - Lengkapi data pengiriman dan jenis ekspedisi yang diinginkan, lalu klik lanjutkan pembayaran </p>
<p> - Lakukan pembayaran sesuai prosedur yang ada di halaman terakhir dari My Cart Anda</p>
<br>
<h3>Pembelian menggunakan kartu kredit secara online</h3>
<p>Kami menerima pembayaran dengan kartu kredit jenis mastercard atau visa.</p>
<p>Pembayaran dengan kartu kredit akan dikenakan biaya administrasi minimal 3,2% dari total jumlah transaksi (besarnya biaya administrasi bergantung pada jenis pembayaran yang dipilih)</p>
